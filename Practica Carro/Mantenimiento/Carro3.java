package Mantenimiento;

public class Carro3 {
    //atributos
    private String marca;
    private String modelo;
    private String color;
    private int anio;
    private String placas;
    private float klmrecorridos;
    private float litrosdegas;

    //Atributos

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getAnio() {
        return anio;
    }

    public void setAnio(int anio) {
        this.anio = anio;
    }

    public String getPlacas() {
        return placas;
    }

    public void setPlacas(String placas) {
        this.placas = placas;
    }

    public float getKlmrecorridos() {
        return klmrecorridos;
    }

    public void setKlmrecorridos(float klmrecorridos) {
        this.klmrecorridos = klmrecorridos;
    }

    public float getLitrosdegas() {
        return litrosdegas;
    }

    public void setLitrosdegas(float litrosdegas) {
        this.litrosdegas = litrosdegas;
    }
}

